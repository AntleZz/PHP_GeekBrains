<?php
$region = [
		"Московская область:" => ["Москва", "Зеленоград", "Клин"],
		"Ленинградская область:" => ["Санкт-Петербург", "Всеволожск", "Кронштадт"],
		"Рязанская область:" => ["Рязань","Касимов","Новомичуринск"]
];
$str = "";
	foreach($region as $reg => $city) {
		$str .= "$reg:<br/>";
		foreach($city as $city1) {
			if(mb_substr($city1, 0,1) == "К") {
				$str .= "$city1, ";
			}
		}
		$str = rtrim($str);	
		$str .= ".<br/>";
	}
	echo $str;